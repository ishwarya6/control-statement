package com.iterative;

public class For_Loop {
	public static void main(String[] args) {
		
		//for(int initialization; condition; iteration)
		for(int i=0; i<=5; i++) {
			System.out.println(i);
			}
		System.out.println("*********");
		
		for(int i=15; i>=10; i--) {
			System.out.println(i);
		}
	
		
	}
	

}
