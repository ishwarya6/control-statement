package com.iterative;

public class For_Each_Loop {
	public static void main(String[] args) {
		
		 int ishu[]= {10,11,12,13,14};
		  
		  for(int i:ishu) { System.out.println(i); }
		 
		
		String size[]= {"S", "L", "M", "XL", "XXL"};
		
		for(String s:size)
		{
			System.out.println(s);
		}
	}

}
